birinci_sayi = int(input("Lütfen bir sayı giriniz..."))
ikinci_sayi = int(input("Lütfen bir sayı daha giriniz..."))
girilen_islem = input("+,-,*,/ Lütfen bir işlem seçiniz...")

if(girilen_islem == "+"):
    input(birinci_sayi + ikinci_sayi)

elif(girilen_islem == "-"):
    input(birinci_sayi - ikinci_sayi)

elif(girilen_islem == "*"):
    input(birinci_sayi * ikinci_sayi)

else:
    (girilen_islem == "/")
    input(birinci_sayi / ikinci_sayi)




